/**
 * 练习 Math 类的使用
 */
public class MathTest {
    public static void main(String[] args) {
        // Math.IP
        double pi = Math.PI;
        System.out.println(pi);
        // Math.E
        double e = Math.E;
        System.out.println(e);
        // Math.sin()
        double sin = Math.sin(30);
        System.out.println(sin);
        // Math.cos()
        double cos = Math.cos(45);
        System.out.println(cos);
        // Math.toRadians()
        double v = Math.toRadians(45);
        System.out.println(v);
        // Math.exp()
        // Math.log()
        // Math.log10()
        // Math.sqrt()
        double sqrt = Math.sqrt(4);
        System.out.println(sqrt);
        // Math.ceil()
        double ceil = Math.ceil(2.7);
        System.out.println(ceil);
        // Math.floor()
        double floor = Math.floor(2.7);
        System.out.println(floor);
        // Math.rint()
        double rint = Math.rint(1.8);
        System.out.println(rint);
        // Math.pow()
        double pow = Math.pow(2, 3);
        System.out.println(pow);
        // Math.round()
        long round = Math.round(3.567);
        System.out.println(round);
        // Math.random()
        double random = Math.random();
        System.out.println(random);
        // Math.max()
        int max = Math.max(23, 66);
        System.out.println(max);
        // Math.min()
        int min = Math.min(23, 66);
        System.out.println(min);
        // Math.abs()
        int abs = Math.abs(-10);
        System.out.println(abs);
        // Math.cbrt
    }
}
